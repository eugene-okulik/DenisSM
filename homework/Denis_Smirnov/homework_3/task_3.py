#Даны два числа. Найти среднее арифметическое и среднее геометрическое этих чисел
a = 5
b = 3

ab_arithmetic_mean = (a + b) / 2
ab_geometric_mean = (a * b)**0.5

print(f"Среднее арифметическое для {a} и {b} = {ab_arithmetic_mean}")
print(f"Среднее геометрическое для {a} и {b} = {ab_geometric_mean}")